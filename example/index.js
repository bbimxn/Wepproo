const express = require("express");
const path = require("path");
const port = 3000;

//ของใหม่ที่เพิ่มมา
const sqlite3 = require('sqlite3').verbose();

// Creating the Express server
const app = express();

// Connect to SQLite database
let db = new sqlite3.Database('employees.db', (err) => {    //ใส่ชื่อไฟล์ database + ถ้าใส่ผิดไปก็จะสร้างฐานข้อมูลเปล่าในชื่อนั้นให้
    if (err) {
        return console.error(err.message);
    }
    console.log('Connected to the SQlite database.');
});


// static resourse & templating engine
app.use(express.static('public'));
// Set EJS as templating engine
app.set('view engine', 'ejs');


// routing path

app.get('/', function (req, res) {
    res.render('home');
})

app.get('/create', function (req, res) {
    const sql = ` CREATE TABLE employees (
    EmployeeId INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
    LastName NVARCHAR(20)  NOT NULL,
    FirstName NVARCHAR(20)  NOT NULL,
    Title NVARCHAR(30),
    Phone NVARCHAR(24),
    Email NVARCHAR(60) ); `;

    db.run(sql, (err) => {
        if (err) {
            return console.error('Error creating table:', err.message);
        }
        console.log('Table created successful');
    });
})


app.get('/show', function (req, res) {
    const query = 'SELECT * FROM employees; ';
    db.all(query, (err, rows) => {
        if (err) {
            console.log(err.message);
        }
        console.log(rows);
        res.render('show', { data: rows });
    });
});

app.get('/delete/:id', function (req, res) {
    // Deleting Data
    let sql = `DELETE FROm employees WHERE EmployeeID =  ${req.params.id}`;
    // delete a row based on id
    db.run(sql, function (err) {
        if (err) {
            return console.error(err.message);
        }
        console.log(`Row(s) deleted.`);
        res.redirect('/show');
    });
});

app.get('/form', function (req, res) {
    res.sendFile(path.join(__dirname, "/public/form.html"));
});


app.get('/formget', function (req, res) {
    const {id,fname,lname,title,phone,email} = req.query;
    //
    let sql = `INSERT INTO  employees (EmployeeId, LastName, FirstName, Title, Phone, Email) VALUES (?,?,?,?,?,?)`;
    console.log(sql);
    db.run(sql, [id,fname,lname,title,phone,email], (err) => {
        if (err) {
            return console.error('Error inserting data:', err.message);
        }
        console.log('Data inserted successful');     
        res.redirect('/show');
    });
});


// Starting the server
app.listen(port, () => {
    console.log("Server started.");
});